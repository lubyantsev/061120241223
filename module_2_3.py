# Исходный список
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Начальный индекс
index = 0

# Цикл while для перебора элементов списка
while index < len(my_list):
    # Получаем текущее число
    number = my_list[index]

    # Проверка на отрицательное число
    if number < 0:
        break  # Прерываем цикл, если число отрицательное

    # Пропускаем нули, так как они ни положительные, ни отрицательные
    if number == 0:
        index += 1  # Увеличиваем индекс и продолжаем
        continue

    # Если число положительное, выводим его
    print(number)

    # Увеличиваем индекс для следующей итерации
    index += 1